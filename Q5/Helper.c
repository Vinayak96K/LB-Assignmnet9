void ArrayReverse(int *iArr,int iSize)
{
    int iStart=0,iEnd=iSize-1,iTemp;
    if((iArr==NULL)||(iSize<=0))
    {
        printf("Incorrect Input!\n");
        return;
    }

    while(iStart<=iEnd)
    {
        iTemp=iArr[iStart];
        iArr[iStart]=iArr[iEnd];
        iArr[iEnd]=iTemp;

        iStart++;
        iEnd--;
    }
}